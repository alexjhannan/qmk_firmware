bool process_record_user(uint16_t keycode, keyrecord_t *record) {
  switch (keycode) {
    case QWERTY:
      if (record->event.pressed) {
        set_single_persistent_default_layer(_QWERTY);
      }
      return false;
      break;
    case LOWER:
      if (record->event.pressed) {
        layer_on(_LOWER);
      } else {
        layer_off(_LOWER);
      }
      return false;
      break;
    case RAISE:
      if (record->event.pressed) {
        layer_on(_RAISE);
      } else {
        layer_off(_RAISE);
      }
      return false;
      break;
    case ADJUST:
      if (record->event.pressed) {
        layer_on(_ADJUST);
      } else {
        layer_off(_ADJUST);
      }
      return false;
      break;
    case GAME:
      if (record->event.pressed) {
        set_single_persistent_default_layer(_GAME);
      }
      return false;
      break;
    case GAMEMOD:
      if (record->event.pressed) {
        layer_on(_GAMEMOD);
      } else {
        layer_off(_GAMEMOD);
      }
      return false;
      break;
    case CHAT:
      if (record->event.pressed) {
         SEND_STRING(SS_TAP(X_ENT));
         set_single_persistent_default_layer(_CHAT);
      }
      return false;
      break;
    case EXITCHATENTER:
      if (record->event.pressed) {
        SEND_STRING(SS_TAP(X_ENT));
        set_single_persistent_default_layer(_GAME);
      }
      return false;
      break;
    case EXITCHATESCAPE:
      if (record->event.pressed) {
        SEND_STRING(SS_TAP(X_ESC));
        set_single_persistent_default_layer(_GAME);
      }
      return false;
      break;
  }
  return true;
}

void encoder_update_user(uint8_t index, bool clockwise) {
    if (index == 0) {
        if (clockwise) {
            tap_code(KC_VOLU);
        } else {
            tap_code(KC_VOLD);
        }
    }
    else if (index == 1) {
        if (clockwise) {
            tap_code(KC_PGDN);
        } else {
            tap_code(KC_PGUP);
        }
    }
}
